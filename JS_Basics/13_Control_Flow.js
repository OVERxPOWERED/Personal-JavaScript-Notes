// 1. if statement
// Syntax: 
// if (condition) {
//     // code block
//   }
// if the condition is true, then the code block will be executed, otherwise not.

// 2. if else statement
// Syntax: 
// if (condition) {
//     // code block
//   } else {
//     // code block
//   }
// if the condition is true, then the code block inside if will be executed, otherwise the code block inside else will be executed.

// 3. if else if statement
// Syntax: 
// if (condition1) {
//     // code block
//   } else if (condition2) {
//     // code block
//   } else {
//     // code block     
//   }
// if the condition1 is true, then the code block inside if will be executed, otherwise if the condition2 is true, then the code block inside else if will be executed, otherwise the code block inside else will be executed.

// 4. Nested if statement
// Syntax:
// if (condition1) {
//     // code block
//     if (condition2) {
//       // code block
//     }
//   }
// if the condition1 is true, then the code block inside if will be executed, otherwise the code block inside else will be executed.
// if the condition2 is true, then the code block inside if will be executed, otherwise not.

// 5. switch statement
// Syntax:
// switch (expression) {
//     case x:
//       // code block
//       break;
//     case y:
//       // code block
//       break;
//     default:
//       // code block
//   }
// The switch statement is used to perform different actions based on different conditions.
// The expression is evaluated once, and the code block that corresponds to the value of the expression is executed.
// Once any case is matched, all the code after that case will be executed except default code block, this happens only when there is no break statement after the code block of a case.

// 6. Ternary Operator
// Syntax:
// (condition) ? value1/code1 : value2/code2
// If the condition is true, the ternary operator returns value1 or executes code1, otherwise it returns value2 or executes code2.
